    <h2 style="background-color: rgb(6, 100, 102); color: white; font-weight: bold;">Historia Szczecina<h2>
            <p2 style="font-size: 18px; background-color: rgb(6, 100, 102); color: white;">
                – obejmuje dzieje miasta od powstania słowiańskiej osady w I tys. n.e. po czasy współczesne. Szczecin założyli Wkrzanie[1] na przełomie VII/VIII wieku na dzisiejszym Wzgórzu Zamkowym. Początkowo była to typowa osada rolników i hodowców, jednak wraz z rozwojem handlu i rzemiosła przebudowano ją do grodu. W takiej postaci w X wieku opisywał Szczecin Ibrahim ibn Jakub, który nazwę miejscowości zapisał jako Sadżin.

                Szczecin był ważnym ośrodkiem kultu pogańskiego, kiedy w 1121 zdobył go Bolesław Krzywousty, który zmusił mieszkańców do chrztu. Jednak ani Polska, ani nieco później Dania nie utrzymały długo kontroli nad miastem – ostatecznie weszło ono do domeny cesarskiej.

                Szczecin, który otrzymał prawa miejskie w 1243, należał do głównych miast Hanzy i księstwa pomorskiego. W późnym średniowieczu w mieście zaczęła dominować ludność niemiecka – stan ten trwał aż do 1945 roku.

                Po wojnie trzydziestoletniej Szczecin przypadł Szwecji, która w 1720 roku utraciła miasto na rzecz Prus. W okresie wojen napoleońskich był okupowany przez Francuzów.

                Wiek XIX to okres rozwoju przemysłu (przede wszystkim stoczniowego), transportu kolejowego oraz – od zniesienia statusu twierdzy w 1873 – dynamicznej rozbudowy miasta.</p2>
